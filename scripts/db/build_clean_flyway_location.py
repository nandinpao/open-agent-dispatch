#!/usr/bin/env python3
"""Build a new-install Flyway location that keeps versions/DDL but neutralizes business DML.

This generator is intentionally conservative. It does not alter historical source files.
Every neutralized statement is replaced by an auditable SQL comment so Flyway version
continuity remains intact. Generated output is disposable and must be reviewed/tested
against PostgreSQL before release.
"""
from __future__ import annotations
import argparse, json, re, shutil
from pathlib import Path
ROOT=Path(__file__).resolve().parents[2]
SOURCE=ROOT/'ai-event-gateway-core/database-platform/src/main/resources/db/migration'
BUSINESS_TABLES={
 'dispatch_flows','dispatch_policies','flow_required_skills','flow_agent_assignments',
 'agent_capability_catalog','agent_capability_assignments','agent_assignment_profiles',
 'assignment_profile_capability_bindings','agent_skill_registry','task_definitions',
 'source_systems','source_system_catalog','source_system_dispatch_defaults',
 'agent_source_assignments','task_requirement_evidence','action_catalog','agent_action_grants',
 'proposed_actions','action_approval_requests','action_approval_decisions',
 'effectful_action_task_links','effectful_action_evidence','agents','tasks'
}
HEAD=re.compile(r'^\s*(insert\s+into|update|delete\s+from)\s+(?:public\.)?([A-Za-z_][A-Za-z0-9_]*)\b',re.I|re.S)
ANY_DML=re.compile(r'\b(insert\s+into|update|delete\s+from)\s+(?:public\.)?([A-Za-z_][A-Za-z0-9_]*)\b',re.I|re.S)

def split_sql(text:str):
 out=[]; start=0; i=0; quote=None; dollar=None; line_comment=False; block=0
 while i<len(text):
  if line_comment:
   if text[i]=='\n': line_comment=False
   i+=1; continue
  if block:
   if text.startswith('/*',i): block+=1; i+=2; continue
   if text.startswith('*/',i): block-=1; i+=2; continue
   i+=1; continue
  if dollar:
   if text.startswith(dollar,i): i+=len(dollar); dollar=None
   else: i+=1
   continue
  if quote:
   if text[i]==quote:
    if i+1<len(text) and text[i+1]==quote: i+=2; continue
    quote=None
   i+=1; continue
  if text.startswith('--',i): line_comment=True; i+=2; continue
  if text.startswith('/*',i): block=1; i+=2; continue
  if text[i] in "'\"": quote=text[i]; i+=1; continue
  if text[i]=='$':
   m=re.match(r'\$[A-Za-z_0-9]*\$',text[i:])
   if m: dollar=m.group(0); i+=len(dollar); continue
  if text[i]==';': out.append(text[start:i+1]); start=i+1
  i+=1
 if start<len(text): out.append(text[start:])
 return out

def active_prefix(statement:str)->str:
 # Remove leading whitespace/comments only for classification.
 s=statement
 while True:
  n=re.sub(r'^\s+','',s)
  if n.startswith('--'):
   pos=n.find('\n'); s='' if pos<0 else n[pos+1:]; continue
  if n.startswith('/*'):
   pos=n.find('*/'); s='' if pos<0 else n[pos+2:]; continue
  return n

def transform(text:str,filename:str):
 neutralized=[]; parts=[]
 for statement in split_sql(text):
  matches=[m for m in ANY_DML.finditer(statement) if m.group(2).lower() in BUSINESS_TABLES]
  if matches:
   neutralized.extend({'operation':re.sub(r'\s+','_',m.group(1).upper()),'table':m.group(2).lower()} for m in matches)
   summary=', '.join(sorted({m.group(2).lower() for m in matches}))
   parts.append(f"\n-- P6 CLEAN BASELINE: neutralized business-data statement touching {summary} from {filename}.\n")
  else: parts.append(statement)
 return ''.join(parts),neutralized

def main():
 ap=argparse.ArgumentParser(); ap.add_argument('--output',required=True); ap.add_argument('--report',required=True)
 a=ap.parse_args(); out=Path(a.output); report_path=Path(a.report)
 if out.exists(): shutil.rmtree(out)
 out.mkdir(parents=True)
 report=[]
 for src in sorted(SOURCE.glob('V*.sql')):
  transformed,items=transform(src.read_text(encoding='utf-8'),src.name)
  (out/src.name).write_text(transformed,encoding='utf-8')
  if items: report.append({'file':src.name,'neutralized':items})
 (out/'README.md').write_text('# Generated clean Flyway location\n\nGenerated by `scripts/db/build_clean_flyway_location.py`. Historical source migrations are unchanged. Business-data DML is neutralized while version continuity and DDL remain. Validate this generated location against an empty PostgreSQL database before release.\n',encoding='utf-8')
 report_path.parent.mkdir(parents=True,exist_ok=True)
 report_path.write_text(json.dumps({'source':str(SOURCE.relative_to(ROOT)),'generatedLocation':str(out),'filesWithNeutralizedBusinessData':len(report),'files':report},indent=2)+'\n',encoding='utf-8')
 print(f'Generated {len(list(out.glob("V*.sql")))} migrations; neutralized business DML in {len(report)} files.')
 return 0
if __name__=='__main__': raise SystemExit(main())
